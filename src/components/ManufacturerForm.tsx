'use client';

import { useState, useEffect } from 'react';
import { Manufacturer } from '@/lib/types';

interface ManufacturerFormProps {
  manufacturer?: Manufacturer | null;
  onSubmit: (manufacturer: Omit<Manufacturer, 'id'>) => void;
  onCancel: () => void;
}

export default function ManufacturerForm({ manufacturer, onSubmit, onCancel }: ManufacturerFormProps) {
  const [formData, setFormData] = useState<Omit<Manufacturer, 'id'>>({
    company: '',
    year: undefined,
    subsetname: ''
  });

  useEffect(() => {
    if (manufacturer) {
      setFormData({
        company: manufacturer.company || '',
        year: manufacturer.year,
        subsetname: manufacturer.subsetname || ''
      });
    }
  }, [manufacturer]);

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    onSubmit(formData);
  };

  const handleChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    const { name, value } = e.target;
    setFormData(prev => ({
      ...prev,
      [name]: value === '' ? undefined : (name === 'year' ? parseInt(value) || undefined : value)
    }));
  };

  return (
    <div className="fixed inset-0 bg-black bg-opacity-50 flex items-center justify-center p-4 z-50">
      <div className="bg-white rounded-lg p-6 w-full max-w-md">
        <h2 className="text-xl font-bold mb-4">
          {manufacturer ? 'Edit Manufacturer' : 'Add New Manufacturer'}
        </h2>
        
        <form onSubmit={handleSubmit} className="space-y-4">
          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Company *
            </label>
            <input
              type="text"
              name="company"
              value={formData.company || ''}
              onChange={handleChange}
              required
              className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
            />
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Year
            </label>
            <input
              type="number"
              name="year"
              value={formData.year?.toString() || ''}
              onChange={handleChange}
              className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
            />
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-700 mb-1">
              Subset Name
            </label>
            <input
              type="text"
              name="subsetname"
              value={formData.subsetname || ''}
              onChange={handleChange}
              className="w-full px-3 py-2 border border-gray-300 rounded-md focus:outline-none focus:ring-2 focus:ring-blue-500"
            />
          </div>

          <div className="flex space-x-3 pt-4">
            <button
              type="submit"
              className="flex-1 bg-blue-600 text-white py-2 px-4 rounded-md hover:bg-blue-700 focus:outline-none focus:ring-2 focus:ring-blue-500"
            >
              {manufacturer ? 'Update' : 'Add'} Manufacturer
            </button>
            <button
              type="button"
              onClick={onCancel}
              className="flex-1 bg-gray-300 text-gray-700 py-2 px-4 rounded-md hover:bg-gray-400 focus:outline-none focus:ring-2 focus:ring-gray-500"
            >
              Cancel
            </button>
          </div>
        </form>
      </div>
    </div>
  );
}